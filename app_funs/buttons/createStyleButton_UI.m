TempAxe.Layout.Column = 2;
TempAxe.Toolbar.Visible = 'off';

ColorBar = colorbar( TempAxe, 'Units', 'pixels', ...
    'AxisLocation', 'in', ...
    'Position', [ 225, 22, 25, 200 ], ...
    'FontName', 'Times New Roman', ...
    'FontSize', 12 );