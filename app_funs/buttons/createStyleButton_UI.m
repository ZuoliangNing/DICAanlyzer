    'Text', DisplayNames.StyleButton_AllStageCheckBox );
AllStageCheckBox.Layout.Column = [1,2];
AllStageCheckBox.Layout.Row = 3;

% ---------- CLimSlider ----------
CLimSlider = uislider( GridLayoutCLimPanel, 'range' );
CLimSlider.Layout.Row = 4;
CLimSlider.Layout.Column = [1,4];
% CLimSlider.Layout.Column = 2;


% ---------- TempAxe ----------
