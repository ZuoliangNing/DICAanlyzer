    'Text', DisplayNames.StyleButton_MinLabel, ...
    'HorizontalAlignment', 'right', ...
    'WordWrap', 'on' );
MinLabel.Layout.Column = 3;
MinLabel.Layout.Row = 3;

% ---------- MinEdit ----------
MinEdit = uieditfield( GridLayoutCLimPanel, 'numeric', ...
    'Value', 0 );
MinEdit.Layout.Column = 4;
MinEdit.Layout.Row = 3;

% ---------- AllStageCheckBox ----------
